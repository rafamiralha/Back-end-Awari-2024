const soma = (primeiroNumero,segundoNumero) =>{
    return Number(primeiroNumero) + Number(segundoNumero)
}

const subtracao = (primeiroNumero,segundoNumero) =>{
    return Number(primeiroNumero) - Number(segundoNumero)
}
//método para exportar MAIS de um modulo
export {soma, subtracao};
